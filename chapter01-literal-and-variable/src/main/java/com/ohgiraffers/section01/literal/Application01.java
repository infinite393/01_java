package main.java.com.ohgiraffers.section01.literal;

public class Application01 {
    public static void main(String[] args) {
        System.out.println("Hello World :D");

        /*
        * 여러가지 값을 출력할 수 있다
        */
        
        /* 목차 1. 숫자 형태의 값 */
        
        System.out.println("123"); //정수형 리터럴
        System.out.println("1.23"); //실수형 리터럴
        
        /* 목차 2. 문자 형태의 값 출력 */
        System.out.println('a'); //문자형 리터럴
        System.out.println('1'); //홀따움표는 문자 하나만 출력 가능 (두개 이상은 에러)
        
        /* 목차 3. 문자열 형태의 값 출력 */
        System.out.println("안녕하세요"); //문자열 형 리터럴

        /* 목차 4. 논리형 형태의 값 출력 */
        System.out.println(true);
        System.out.println(false);



    }
}
